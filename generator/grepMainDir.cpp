#include <iostream>

using namespace std;

int main() {
  unsigned long long x, y, z, w;
  while (cin) {
    unsigned long long nx, ny, nz, nw;
    cin >> nx >> ny >> nz >> nw;
    x = nx;
    y = ny;
    z = nz;
    w = nw;
  }
  //cout << x << "\t" << y << "\t" << z << "\t" << w;
  cout << x << " " << y << " " << z << " " << w;
  return 0;
}
